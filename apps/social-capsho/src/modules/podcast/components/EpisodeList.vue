<script setup>
  import { computed, ref } from 'vue'
  import { submitFileForTranscription } from '@/modules/podcast/services/transcription'
  import { useStore } from 'vuex'
  import { addTranscriptToCollection } from '../services/transcript'
  import { makeTranscript } from '@/entities/podcast'
  import { useAuth } from '@/composables/useAuth'
  import { v4 as uuidv4 } from 'uuid'
  import { useNotification } from '@/composables/useNotification'

  defineProps({
    episodes: {
      type: Array,
      required: true
    }
  })

  const $store = useStore()
  const podcast = computed(() => $store.getters['podcast/getPodcastInfo'])
  const transcriptions = ref({})
  const savedTranscriptions = computed(() => $store.getters['podcast/getTranscripts'])
  const episodeTranscriptionLoading = ref([])

  // Function to get a transcription from Deepgram, passing in the episode url
  async function convertAudioToText(episode) {
    try {
      // setting the loading state to true for the episode
      episodeTranscriptionLoading.value.push(episode.guid)
      // setting a unique id for the episode transcription
      transcriptions.value[`${podcast.value.podcast_id}---${episode.guid}`] = await submitFileForTranscription({
        url: episode.url
      })
      // removing the loading state for the episode
      episodeTranscriptionLoading.value.splice(
          episodeTranscriptionLoading.value.indexOf(episode.guid),
          1
      )
    } catch (e) {
      console.error(e.message)
    }
  }
  const { alertWarning, alertSuccess } = useNotification()
  function saveTranscription(podcastId, episode) {
    try {
      const { authUser } = useAuth()
      const Transcript = makeTranscript({
        user_id: authUser.value.uid,
        transcript_id: uuidv4(),
        title: episode.title,
        podcast_id: podcastId,
        episode_guid: episode.guid,
        transcript: transcriptions.value[`${podcastId}---${episode.guid}`].channels[0].alternatives[0].transcript
      })
      addTranscriptToCollection({
        user_id: Transcript.getUserId(),
        transcript_id: Transcript.getTranscriptId(),
        title: Transcript.getTitle(),
        episode_guid: Transcript.getEpisodeGuid(),
        transcript: Transcript.getTranscript(),
        podcast_id: Transcript.getPodcastId(),
        created_at: Transcript.getCreatedAt()
      }).then(() => {
        alertSuccess('Transcript Saved!')
      })
    } catch (e) {
      alertWarning(e)
    }
  }
</script>

<template>
  <div class="flex flex-col px-6 py-3">
    <h2 class="">
      Episodes
    </h2>
    <ul class="">
      <li
        v-for="episode in episodes.slice().reverse()"
        :key="episode.guid || episode.link"
        class=""
      >
        <div class="py-3">
          <div class="flex items-center">
            <div class="basis-1/5">
              <button
                class="rounded-full bg-yellow-300 h-14 w-14 text-black"
              >
                Play
              </button>
            </div>
            <div
              class="basis-4/5 px-3 flex flex-col items-start"
            >
              <h3 class="text-gray-800 text-md">
                {{ episode.title }}
              </h3>
              <div class="flex flex-row items-baseline">
                <button
                  v-if="savedTranscriptions[`${podcast.podcast_id}---${episode.guid}`]"
                  class="disabled:opacity-75 appearance-none underline text-gray-400 font-thin"
                  disabled
                >
                  Transcription Saved
                </button>
                <button
                  v-else-if="!transcriptions[`${podcast.podcast_id}---${episode.guid}`]"
                  class="underline text-gray-400 font-thin"
                  @click.prevent="convertAudioToText(episode)"
                >
                  <svg
                    v-if="episodeTranscriptionLoading.includes(episode.guid)"
                    class="inline mr-2 w-4 h-4 text-gray-200 animate-spin dark:text-gray-600"
                    viewBox="0 0 100 101"
                    fill="none"
                    xmlns="http://www.w3.org/2000/svg"
                  >
                    <path
                      d="M100 50.5908C100 78.2051 77.6142 100.591 50 100.591C22.3858 100.591 0 78.2051 0 50.5908C0 22.9766 22.3858 0.59082 50 0.59082C77.6142 0.59082 100 22.9766 100 50.5908ZM9.08144 50.5908C9.08144 73.1895 27.4013 91.5094 50 91.5094C72.5987 91.5094 90.9186 73.1895 90.9186 50.5908C90.9186 27.9921 72.5987 9.67226 50 9.67226C27.4013 9.67226 9.08144 27.9921 9.08144 50.5908Z"
                      fill="currentColor"
                    />
                    <path
                      d="M93.9676 39.0409C96.393 38.4038 97.8624 35.9116 97.0079 33.5539C95.2932 28.8227 92.871 24.3692 89.8167 20.348C85.8452 15.1192 80.8826 10.7238 75.2124 7.41289C69.5422 4.10194 63.2754 1.94025 56.7698 1.05124C51.7666 0.367541 46.6976 0.446843 41.7345 1.27873C39.2613 1.69328 37.813 4.19778 38.4501 6.62326C39.0873 9.04874 41.5694 10.4717 44.0505 10.1071C47.8511 9.54855 51.7191 9.52689 55.5402 10.0491C60.8642 10.7766 65.9928 12.5457 70.6331 15.2552C75.2735 17.9648 79.3347 21.5619 82.5849 25.841C84.9175 28.9121 86.7997 32.2913 88.1811 35.8758C89.083 38.2158 91.5421 39.6781 93.9676 39.0409Z"
                      fill="#1C64F2"
                    />
                  </svg>
                  {{ episodeTranscriptionLoading.includes(episode.guid) ? 'Transcribing...' : 'Get Transcription' }}
                </button>
                <button
                  v-if="transcriptions[`${podcast.podcast_id}---${episode.guid}`] && !savedTranscriptions[`${podcast.podcast_id}---${episode.guid}`]"
                  class="underline text-gray-400 font-thin"
                  @click.prevent="saveTranscription(podcast.podcast_id, episode)"
                >
                  Save Transcription
                </button>
                <span class="pl-3 text-sm">
                  {{ episode.duration }}
                </span>
              </div>
            </div>
          </div>
        </div>
        <div
          v-if="savedTranscriptions[`${podcast.podcast_id}---${episode.guid}`]"
          class="transcription"
        >
          <p>
            {{ savedTranscriptions[`${podcast.podcast_id}---${episode.guid}`].transcript }}
          </p>
        </div>
        <div
          v-else-if="transcriptions[`${podcast.podcast_id}---${episode.guid}`]"
          class="transcription"
        >
          <p>
            {{ transcriptions[`${podcast.podcast_id}---${episode.guid}`].channels[0].alternatives[0].transcript }}
          </p>
        </div>
      </li>
    </ul>
  </div>
</template>
